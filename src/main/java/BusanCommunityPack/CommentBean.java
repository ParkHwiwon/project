package BusanCommunityPack;

public class CommentBean {

	private int commentSeq;
	private int commentBoard;
	private String commentWriter;
	private String commentContent;
	private String commentRegdate;
	private int commentPos;
	private int commentRef;
	private int commentDepth;
	private String commentIp;


	public int getCommentSeq() {
		return commentSeq;
	}
	
	public void setCommentSeq(int commentSeq) {
		this.commentSeq = commentSeq;
	}
	
	public int getCommentBoard() {
		return commentBoard;
	}

	public void setCommentBoard(int commentBoard) {
		this.commentBoard = commentBoard;
	}
	
	public String getCommentWriter() {
		return commentWriter;
	}
	
	public void setCommentWriter(String commentWriter) {
		this.commentWriter = commentWriter;
	}
	
	public String getCommentContent() {
		return commentContent;
	}
	
	public void setCommentContent(String commentContent) {
		this.commentContent = commentContent;
	}
	
	public int getCommentPos() {
		return commentPos;
	}

	public void setCommentPos(int commentPos) {
		this.commentPos = commentPos;
	}
	
	public String getCommentRegdate() {
		return commentRegdate;
	}
	
	public void setCommentRegdate(String commentRegdate) {
		this.commentRegdate = commentRegdate;
	}

	public int getCommentRef() {
		return commentRef;
	}
	
	public void setCommentRef(int commentRef) {
		this.commentRef = commentRef;
	}
	
	public int getCommentDepth() {
		return commentDepth;
	}

	public void setCommentDepth(int commentDepth) {
		this.commentDepth = commentDepth;
	}
	
	public String getCommentIp() {
		return commentIp;
	}
	
	public void setCommentIp(String commentIp) {
		this.commentIp = commentIp;
	}
	
	
	
	
	
}
